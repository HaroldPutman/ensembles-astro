/**
 * Rebuild activity old->new URL map from git history + frontmatter rules.
 * Run after migrate-activity-files.mjs: node scripts/generate-activity-redirects.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { execSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

function parseStartDateFromFm(fm) {
  const sdLine = fm.split('\n').find(l => l.startsWith('startDate:'));
  if (!sdLine) return null;
  const raw = sdLine.split(':').slice(1).join(':').trim();
  const parts = raw.split('/');
  if (parts.length !== 3) return null;
  const month = parseInt(parts[0], 10);
  const day = parseInt(parts[1], 10);
  const year = parseInt(parts[2], 10);
  return {
    yyyy: String(year),
    mm: String(month).padStart(2, '0'),
    dd: String(day).padStart(2, '0'),
  };
}

function slugFromBasename(basename, start) {
  if (/^gather2026[1-9]$/.test(basename)) {
    return 'gather' + basename.slice(-1);
  }
  let m = basename.match(/^(.+)-(\d{6})$/);
  if (m) return m[1];
  m = basename.match(/^(.+?)(\d{8})$/);
  if (m) {
    const base = m[1];
    if (base === 'pour') return `pour-${start.dd}`;
    return base;
  }
  m = basename.match(/^(.+?)(\d{6})$/);
  if (m) return m[1];
  m = basename.match(/^(.+?)(?<![0-9])(\d{4})$/);
  if (m) return m[1];
  throw new Error('No slug rule for: ' + basename);
}

const out = execSync(
  'git ls-tree -r HEAD --name-only collections/activities',
  { cwd: root, encoding: 'utf8' }
);
const oldPaths = out
  .split('\n')
  .map(s => s.trim())
  .filter(p => p.endsWith('.mdx'));

const rows = [];
for (const gitPath of oldPaths) {
  const fm = execSync(`git show HEAD:${gitPath}`, {
    cwd: root,
    encoding: 'utf8',
    maxBuffer: 2_000_000,
  }).match(/^---\n([\s\S]*?)\n---/);
  if (!fm) throw new Error('No frontmatter in ' + gitPath);
  const start = parseStartDateFromFm(fm[1]);
  if (!start) throw new Error('Bad startDate in ' + gitPath);
  const basename = path.basename(gitPath, '.mdx');
  const slug = slugFromBasename(basename, start);
  const oldId = basename;
  const newId = `${start.yyyy}/${start.mm}/${slug}`;
  const newFsPath = path.join(root, 'collections', 'activities', ...newId.split('/'));
  if (!fs.existsSync(`${newFsPath}.mdx`)) {
    throw new Error(`Missing ${newFsPath}.mdx for old ${oldId}`);
  }
  rows.push({ oldId, newId });
}

const redirects = {};
for (const r of rows) {
  redirects[`/activities/${r.oldId}`] = `/activities/${r.newId}`;
  redirects[`/register/${r.oldId.toLowerCase()}`] =
    `/register/${r.newId.toLowerCase()}`;
  redirects[`/register/info/${r.oldId.toLowerCase()}`] =
    `/register/info/${r.newId.toLowerCase()}`;
}

const dataDir = path.join(root, 'src', 'data');
fs.mkdirSync(dataDir, { recursive: true });
const outRedirects = path.join(dataDir, 'activity-legacy-redirects.mjs');
fs.writeFileSync(
  outRedirects,
  `// Auto-generated by scripts/generate-activity-redirects.mjs\nexport const activityLegacyRedirects = ${JSON.stringify(redirects, null, 2)};\n`
);

const sqlStmts = rows
  .map(
    r =>
      `UPDATE registration SET activity = '${r.newId.replace(/'/g, "''")}' WHERE LOWER(activity) = LOWER('${r.oldId.replace(/'/g, "''")}');`
  )
  .join('\n  ');

const downSql = rows
  .map(
    r =>
      `UPDATE registration SET activity = '${r.oldId.replace(/'/g, "''")}' WHERE LOWER(activity) = LOWER('${r.newId.replace(/'/g, "''")}');`
  )
  .join('\n  ');

const migrationPath = path.join(
  root,
  'migrations',
  '1774000000000_update-registration-activity-paths.js'
);
const migrationBody = `/**
 * @type {import('node-pg-migrate').ColumnDefinitions | undefined}
 */
export const shorthands = undefined;

/**
 * @param pgm {import('node-pg-migrate').MigrationBuilder}
 */
export const up = pgm => {
  pgm.sql(\`
  ${sqlStmts}
  \`);
};

/**
 * @param pgm {import('node-pg-migrate').MigrationBuilder}
 */
export const down = pgm => {
  pgm.sql(\`
  ${downSql}
  \`);
};
`;

fs.writeFileSync(migrationPath, migrationBody);
console.log('Wrote', outRedirects);
console.log('Wrote', migrationPath, '(' + rows.length + ' activities)');
